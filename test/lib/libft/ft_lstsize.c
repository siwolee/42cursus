#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*node;
	int		len;

	len = 0;
	if (lst)
	{
		node = lst;
		len = 1;
		while (node->next != 0)
		{
			node = node->next;
			len++;
		}
	}
	return (len);
}
