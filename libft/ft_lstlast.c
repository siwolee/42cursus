#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*node;

	node = 0;
	if (lst)
	{
		node = lst;
		while (node->next != 0)
		{
			node = node->next;
		}
	}
	return (node);
}
