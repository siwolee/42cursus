#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*ptr;

	ptr = (char *)s;
	if (*ptr == '\0' && c == '\0')
		return (ptr);
	ptr += ft_strlen(ptr) + 1;
	while (ptr != s || c == '\0')
	{
		ptr--;
		if (*ptr == (char)c)
			return (ptr);
	}
	return (0);
}
